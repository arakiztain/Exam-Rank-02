int	ft_atoi(const char *str)
{
	int	i;
	int	r;
	int	s;

	i = 0;
	s = 1;
	r = 0;
	while (str[i] == 32 || (str[i] >= 7 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		s *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
		r = (r * 10) + str[i] - '0';
		i++;
	
	return (r * s);
}